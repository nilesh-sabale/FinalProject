The formula for calculating simple interest is:

Simple Interest (SI) = (Principal × Rate × Time) / 100

Where:

Principal is the initial amount of money invested or borrowed.
Rate is the annual interest rate (in percent).
Time is the period for which the money is invested or borrowed (in years).
Theory Explanation:
Simple interest is a method of calculating the interest charged or earned on a principal amount at a fixed rate over a specific period of time. The interest does not compound, meaning it is calculated only on the original principal, not on accumulated interest. This makes it straightforward and easy to compute for loans, savings, and investments over shorter periods.
